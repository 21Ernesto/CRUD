    private void ctBuscarCategoriaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ctBuscarCategoriaKeyReleased
        // TODO add your handling code here:
        

        mostrarDatos(ctBuscarCategoria.getText());
        
        if(ctBuscarCategoria.getText().equals("")==true){
            ctIdCategoria.setText("");
            ctNombreCategoria.setText("");
            ctDescripcionCategoria.setText(""); 
        }else{
            if(tablaBuscar.getRowCount()>0){  //Si la cantidad de filas de la tabla es mayor a cero.
                ctIdCategoria.setText(tablaBuscar.getValueAt(0, 0).toString());
                ctNombreCategoria.setText(tablaBuscar.getValueAt(0, 1).toString());
                ctDescripcionCategoria.setText(tablaBuscar.getValueAt(0, 2).toString());
            }            
        }
        
    }//GEN-LAST:event_ctBuscarCategoriaKeyReleased

