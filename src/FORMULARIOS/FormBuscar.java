    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        // TODO add your handling code here:
        
        String [] opciones = {"Enviar","Cancelar"};
        int i = JOptionPane.showOptionDialog(null, "¿Deseas enviar estos datos?", "Pregunta",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,null,opciones,opciones[0]);

        if(ctIdCategoria.getText().equals("")==true){
            JOptionPane.showMessageDialog(null,"Seleccione una categoría de la tabla para enviar");
        }else{
            if(i==0){
                JOptionPane.showMessageDialog(null, "Enviando datos . . ");
                new FormPrendas();
                FormPrendas.categoria.setIdcategoria(Integer.parseInt(ctIdCategoria.getText()));
                FormPrendas.categoria.setNombre(ctNombreCategoria.getText());
                FormPrendas.categoria.setDescripcion(ctDescripcionCategoria.getText());
                
                this.dispose();

            }
            else{}
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

