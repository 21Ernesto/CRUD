    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        categoria.setDescripcion(ctDescripcionCategoria.getText());
        categoria.setNombre(ctNombreCategoria.getText());
        
        if(ctNombreCategoria.getText().equals("")==true || ctDescripcionCategoria.getText().equals("")==true){
            JOptionPane.showMessageDialog(null, "Complete todos los campos");
        }else{
            if(control==true){
           //NUEVO
           conexion.guardarCartegoria(categoria);
            }else{
               //MODIFICAR
               categoria.setIdcategoria(Integer.parseInt(ctIdCategoria.getText()));
               conexion.actualizarCategoria(categoria);

            } 
        }
        
        btnNuevo.setEnabled(valor);
        btnGuardar.setEnabled(!valor);
        btnCancelar.setEnabled(!valor);
        btnEditar.setEnabled(valor);
        btnEliminar.setEnabled(valor);

        limpiarTexto();

        ctNombreCategoria.setEnabled(!valor);
        ctDescripcionCategoria.setEnabled(!valor);
        mostrarDatos("");
    }//GEN-LAST:event_btnGuardarActionPerformed

