    public void mostrarDatos(String busqueda){
        ArrayList<Categoria> listaCategoria = new ArrayList<>();
        listaCategoria=conexion.mostrarCategoria(busqueda);
        DefaultTableModel tabla = new DefaultTableModel();
        tabla.addColumn("ID CATEGOÍA");
        tabla.addColumn("NOMBRE");
        tabla.addColumn("DESCRIPCIÓN");
        tabla.setRowCount(listaCategoria.size());
        for(int regActual=0; regActual<listaCategoria.size();regActual=regActual+1){
            tabla.setValueAt(listaCategoria.get(regActual).getIdcategoria(), regActual, 0);
            tabla.setValueAt(listaCategoria.get(regActual).getNombre(), regActual, 1);
            tabla.setValueAt(listaCategoria.get(regActual).getDescripcion(), regActual, 2);
        }
        tablaCategoria.setModel(tabla);        
    }
    
