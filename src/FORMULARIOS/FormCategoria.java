    private void ctBuscarCategoriaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ctBuscarCategoriaKeyReleased
        // TODO add your handling code here:
        mostrarDatos(ctBuscarCategoria.getText());
        
        if(ctBuscarCategoria.getText().equals("")==true){
            limpiarTexto();           
        }else{
            if(tablaCategoria.getRowCount()>0){  //Si la cantidad de filas de la tabla es mayor a cero.
                ctIdCategoria.setText(tablaCategoria.getValueAt(0, 0).toString()   );
                ctNombreCategoria.setText(tablaCategoria.getValueAt(0, 1).toString());
                ctDescripcionCategoria.setText(tablaCategoria.getValueAt(0, 2).toString());
            }           
        }    
    }//GEN-LAST:event_ctBuscarCategoriaKeyReleased

