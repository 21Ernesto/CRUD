    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        if(ctIdCategoria.getText().equals("")==true){
            JOptionPane.showMessageDialog(null, "Debe seleccionar una categoria de la tabla para eliminar");
        }else{
            boolean resultado;
            resultado = conexion.eliminarCategoria(Integer.parseInt(  ctIdCategoria.getText()   )   );
            if(resultado==true){
                JOptionPane.showMessageDialog(null, "Categoría borrado correctamente");
                limpiarTexto();
                mostrarDatos("");
            }else
            {
                JOptionPane.showMessageDialog(null, "No se pudo borrar la categoría");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

