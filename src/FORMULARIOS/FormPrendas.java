    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        boolean resultado;
        if(ctIdPrenda.getText().equals("")==true){
            JOptionPane.showMessageDialog(null, "Debe seleccionar una prenda de la tabla para eliminar");
        }else{
            
            resultado = conexion.eliminarPrenda(Integer.parseInt(ctIdPrenda.getText()));
            
            if(resultado==true){
                JOptionPane.showMessageDialog(null, "Prenda borrada correctamente");
                mostrarDatos("");
            }else
            {
                JOptionPane.showMessageDialog(null, "No se pudo borrar la prenda");
            }
            
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

