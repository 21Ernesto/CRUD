    public void mostrarDatos(String busqueda){
        ArrayList<Prenda> listaPrenda = new ArrayList<>();
        listaPrenda=conexion.mostrarPrenda(busqueda);
        DefaultTableModel tabla = new DefaultTableModel();
        tabla.addColumn("ID PRENDA");
        tabla.addColumn("NOMBRE");
        tabla.addColumn("MODELO");
        tabla.addColumn("TALLA");
        tabla.addColumn("PRECIO");
        tabla.addColumn("STOCK");
        tabla.addColumn("ID CATEGORÍA");
        tabla.setRowCount(listaPrenda.size());
        for(int regActual=0; regActual<listaPrenda.size();regActual=regActual+1){
            tabla.setValueAt(listaPrenda.get(regActual).getIdPrendas(), regActual, 0);
            tabla.setValueAt(listaPrenda.get(regActual).getNombre(), regActual, 1);
            tabla.setValueAt(listaPrenda.get(regActual).getModelo(), regActual, 2);
            tabla.setValueAt(listaPrenda.get(regActual).getTalla(), regActual, 3);
            tabla.setValueAt(listaPrenda.get(regActual).getPrecio(), regActual, 4);
            tabla.setValueAt(listaPrenda.get(regActual).getStock(), regActual, 5);
            tabla.setValueAt(listaPrenda.get(regActual).getFk_idCategoria(), regActual, 6);
        }
        tablaPrenda.setModel(tabla);        
    }
    
