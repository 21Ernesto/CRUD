    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
            // TODO add your handling code here:
        prenda.setNombre(ctNombrePrenda.getText());
        prenda.setModelo(ctModeloPrenda.getText());
        prenda.setTalla(ctTallaPrenda.getText());
        prenda.setPrecio(Double.parseDouble(ctPrecioPrenda.getText()));
        prenda.setStock(Integer.parseInt(ctStockPrenda.getText()));
        prenda.setFk_idCategoria(Integer.parseInt(ctIdCategoria.getText()));
             
        if(ctNombrePrenda.getText().equals("")==true || ctModeloPrenda.getText().equals("")==true || ctTallaPrenda.getText().equals("")==true || ctPrecioPrenda.getText().equals("")==true || ctStockPrenda.getText().equals("")==true){
            JOptionPane.showMessageDialog(null, "Complete todos los campos");
        }else{        
            if(control==true){
               //NUEVO
               conexion.guardarPrenda(prenda);
            }else{
               //MODIFICAR
               prenda.setIdPrendas(Integer.parseInt(ctIdPrenda.getText()));
               conexion.actualizarPrenda(prenda);

            }
        }
        
        btnNuevo.setEnabled(valor);
        btnGuardar.setEnabled(!valor);
        btnCancelar.setEnabled(!valor);
        btnEditar.setEnabled(valor);
        btnEliminar.setEnabled(valor);
        btnSalir.setEnabled(valor);
        
        limpiarTexto();
        
        ctNombrePrenda.setEnabled(!valor);
        ctModeloPrenda.setEnabled(!valor);
        ctTallaPrenda.setEnabled(!valor);
        ctPrecioPrenda.setEnabled(!valor);
        ctStockPrenda.setEnabled(!valor);
        
        mostrarDatos("");
    }//GEN-LAST:event_btnGuardarActionPerformed

